package FuelStation;
/**
 * Интерфейс IFuelStation представляет функциональность, связанную с заправкой
 * автомобилей топливом на заправочной станции.
 */

public interface IFuelStation {

    /**
     * Метод refuel выполняет заправку автомобиля топливом на заправочной станции.
     */
    void refuel();
}